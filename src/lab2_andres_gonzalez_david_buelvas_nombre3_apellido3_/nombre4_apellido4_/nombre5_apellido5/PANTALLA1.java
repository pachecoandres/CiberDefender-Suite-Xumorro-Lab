    private void btnAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnAgregarMouseClicked
        // TODO add your handling code here:
        if (play == false) {
            JOptionPane.showMessageDialog(null, "Presiona INICIO para comenzar.");
            return;
        }
        String seleccionado = listaEventosDeSeguridad.getSelectedValue();

        if (seleccionado == null) {
            JOptionPane.showMessageDialog(null, "Selecciona un evento primero.");
            return;
        }

        boolean esAmenaza = seleccionado.equals("Ataque de phishing")
                || seleccionado.equals("Malware detectado")
                || seleccionado.equals("Acceso no autorizado")
                || seleccionado.equals("Spyware")
                || seleccionado.equals("Ransomware");

        if (esAmenaza) {
            modeloAmenazas.addElement(seleccionado);
            modeloEventos.removeElement(seleccionado);
            puntaje++;
            lblpuntaje.setText("PUNTAJE: " + puntaje);
            JOptionPane.showMessageDialog(null, "✅ Amenaza detectada correctamente");
        } else {
            modeloEventos.removeElement(seleccionado);
            vidas--;
            lblvidas.setText("VIDAS: " + vidas);
            JOptionPane.showMessageDialog(null, "❌ Esto no es una amenaza");
        }

        // Condición de derrota
        if (vidas <= 0) {
            JOptionPane.showMessageDialog(null, "La red fue comprometida.");
            play = false;
        }

        // Condición de victoria
        if (modeloAmenazas.size() >= 5) {
            JOptionPane.showMessageDialog(null, "Felicidades, has asegurado la red");
            PuntajesGlobales.modulo1 = puntaje;
            play = false;
        }
    }//GEN-LAST:event_btnAgregarMouseClicked

