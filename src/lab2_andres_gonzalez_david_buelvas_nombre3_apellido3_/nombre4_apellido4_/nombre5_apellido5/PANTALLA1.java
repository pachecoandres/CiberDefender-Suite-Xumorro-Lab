    private boolean esPhishing(String texto) {
        if (texto == null || texto.isEmpty()) {
            return false;
        }

        texto = texto.toLowerCase();

        String[] sospechosas = {
            "contraseña", "password", "urgente", "clic",
            "aquí", "link", "verificar", "premio", "ganaste", "actualizar"
        };

        for (String palabra : sospechosas) {
            if (texto.contains(palabra)) {
                return true;
            }
        }
        return false;
    }

    private void actualizarPuntaje(boolean acierto) {
        if (acierto) {
            puntaje++;
        } else {
            puntaje--;
            if (puntaje < 0) {
                puntaje = 0;
            }
        }
        lblpuntaje.setText("PUNTAJE: " + puntaje);
    }

    private void guardarResultado(String evento, String tipo) {
        try {
            FileWriter fw = new FileWriter("resultados_modulo1.txt", true);
            fw.write("Evento: " + evento + " | Clasificación: " + tipo + " | Puntaje: " + puntaje + "\n");
            fw.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al guardar el resultado.");
        }
    }

